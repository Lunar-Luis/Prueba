package com.farmacia.farmacia_api.inventario;

import java.time.LocalDate;

public class RecepcionDTO {
    // --- DATOS DEL MEDICAMENTO ---
    private String codigo;
    private String nombre;

    // CAMBIO CLAVE: Separamos los conceptos para que coincidan con tu BD
    private String descripcion;       // Ej: "Caja x 30 Tabletas" (Antes era 'presentacion')
    private String concentracion;     // Ej: "500mg" (NUEVO)
    private String formaFarmaceutica; // Ej: "Tableta" (Coincide con tabla 'presentaciones')

    private String marca;
    private String linea;

    // --- DATOS DEL LOTE ---
    private String lote;
    private LocalDate vencimiento;
    private Integer cantidad;
    private String ubicacion;
    private String proveedor;
    private Integer usuarioId;

    public RecepcionDTO() {}

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getConcentracion() {
        return concentracion;
    }

    public void setConcentracion(String concentracion) {
        this.concentracion = concentracion;
    }

    public String getFormaFarmaceutica() {
        return formaFarmaceutica;
    }

    public void setFormaFarmaceutica(String formaFarmaceutica) {
        this.formaFarmaceutica = formaFarmaceutica;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getLinea() {
        return linea;
    }

    public void setLinea(String linea) {
        this.linea = linea;
    }

    public String getLote() {
        return lote;
    }

    public void setLote(String lote) {
        this.lote = lote;
    }

    public LocalDate getVencimiento() {
        return vencimiento;
    }

    public void setVencimiento(LocalDate vencimiento) {
        this.vencimiento = vencimiento;
    }

    public Integer getCantidad() {
        return cantidad;
    }

    public void setCantidad(Integer cantidad) {
        this.cantidad = cantidad;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getProveedor() {
        return proveedor;
    }

    public void setProveedor(String proveedor) {
        this.proveedor = proveedor;
    }

    public Integer getUsuarioId() {
        return usuarioId;
    }

    public void setUsuarioId(Integer usuarioId) {
        this.usuarioId = usuarioId;
    }
}
